<li aria-level="1"><span>Мизерное напряжение на клеммах;</span></li>
<li aria-level="1"><span>Электролит внутри помутнел или почернел;</span></li>
<li aria-level="1"><span>Сильно греется при заряде и долго остывает;</span></li>
<li aria-level="1"><span>Чувствуется неприятный запах.</span></li>
</ul>
<p><span>При появлении этих признаков стоит задуматься о замене старой АКБ, не дожидаясь, пока ситуация станет критической и придется производить экстренную замену вне стен автосервиса. Это может привести к потере всех настроек бортового компьютера Volvo. Если чувствуете, что аккумулятор уже работает плохо, своевременно обратитесь в надежный техцентр.</span></p>
