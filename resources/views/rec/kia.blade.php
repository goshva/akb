В данном случае, казалось бы, несущественному вопросу стоит годиться со всей серьезностью, так как при несоответствии габаритов АКБ имеется возможность просто элементарно не подойти. Еще выучите подобный параметр как оборотная ассоциация. Это означает, что в случае если разместить аккумулятор напрямик, то слева станет «-»,а справа «+». При оборотной полярности месторасположение полюсов станет обратным.
